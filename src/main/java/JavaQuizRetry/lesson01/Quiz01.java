package JavaQuizRetry.lesson01;

import java.util.Scanner;

public class Quiz01 {

	public static void main(String[] args) {
		// quiz01_1. 정수 출력 및 연산
		System.out.println("이름 : 신보람 나이 : 25");
		int three = 3;
		int five = 5;
		int seven = 7;
		System.out.println(three + " + " + five + " = " + (three + five));
		System.out.println(three + " - " + five + " = " + (three - five));
		System.out.println(seven + " X 1" + " = " + (seven * 1));
		System.out.println(seven + " X 2" + " = " + (seven * 2));
		System.out.println(seven + " X 3" + " = " + (seven * 3));

	}

}
